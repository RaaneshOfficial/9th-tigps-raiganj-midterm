numbers = [23, 12, 5, 9, 65, 44]

# a) Print length of list
print(f"Length of list: {len(numbers)}")

# b) Print elements from second to fourth position using positive indexing
print(f"Elements from 2nd to 4th position: {numbers[1:4]}")

# c) Print elements from 1st to 5th using negative indexing
print(f"Elements from 1st to 5th position using negative indexing: {numbers[-6:-1]}")